package Model;

/**
 *
 * @author agith
 */
public class Journal {
      int jid;
    String date;
    String name;
    double debit;
    double credit;
    String remarks;
    
    public Journal(){
        
    }

    public Journal(int jid, String date, String name, double debit, double credit, String remarks) {
        this.jid = jid;
        this.date = date;
        this.name = name;
        this.debit = debit;
        this.credit = credit;
        this.remarks = remarks;
    }

    public int getJid() {
        return jid;
    }

    public void setJid(int jid) {
        this.jid = jid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getDebit() {
        return debit;
    }

    public void setDebit(double debit) {
        this.debit = debit;
    }

    public double getCredit() {
        return credit;
    }

    public void setCredit(double credit) {
        this.credit = credit;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
    
    
}
